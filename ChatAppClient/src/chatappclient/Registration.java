    public boolean validation()
    {
        String name=jTextField1.getText();
        String uid=jTextField2.getText();
        String pass=jPasswordField1.getText();
        String cpass=jPasswordField2.getText();
        
        if(name.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter your name", "Name Missing", JOptionPane.ERROR_MESSAGE);
            return false;
            
        }
        
        else if(uid.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter your user ID", "UserID missing", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        else if(pass.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter your password", "Password Missing", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        else if(cpass.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please confirm your password", "Confirm Password Missing", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if(cpass.equals(pass)){
            
            return true;
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Passwords did not match!!!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
            
    }


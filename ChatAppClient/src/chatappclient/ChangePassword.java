    public boolean validatePassword()
    {
        String pass=jPasswordField1.getText();
        String npass=jPasswordField2.getText();
        String cnpass=jPasswordField5.getText();
        String opass=new String();
        
        User obj=new User();
        obj.userno=ChatAppClient.LoggedUserNo;
        obj.commandName="#getData#";
        
        try{
            Socket ss=new Socket(ChatAppClient.IPAddress, ChatAppClient.PortNo);
            ObjectOutputStream oos=new ObjectOutputStream(ss.getOutputStream());
            oos.writeObject(obj);
            
            ObjectInputStream ois=new ObjectInputStream(ss.getInputStream());
            User user =(User) ois.readObject();
            
            opass=user.pwd;
            ss.close();
            
        }
        
        catch(Exception ex){
            System.out.println(ex);
        }
        
        if(pass.equals("")||npass.equals("")||cnpass.equals("")){
            JOptionPane.showMessageDialog(null, "Fill in the required fields...");
            return false;
        }
        
        if(pass.equals(opass))
        {
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Password Incorrect!");
            return false;
        }
        
        if(npass.equals(cnpass))
        {
            return true;
        }
        else
        {
            JOptionPane.showMessageDialog(null, "New password and Confirm password do not match");
            return false;
        }
    }

